package com.yashsiddham;

public class ManipulateIntMaxValue {
    public static void main(String[] args) {

        int n = Integer.MAX_VALUE;
        System.out.println("Value of n : " + n);
        System.out.println("Value of ++n : " + ++n);
        System.out.println("Value of n++ : " + n++);
        System.out.println("Value of --n : " + --n);
        System.out.println("Value of n-- : " + n--);

    }
}
